inline ll mul(ll x, ll y, ll m)
{
    ll z = 1LL * x * y;
    if (z >= m)
        z %= m;
    return z;
}
inline ll add(ll x, ll y, ll m)
{
    ll z = x + y;
    if (z >= m)
        z %= m;
    return z;
}
inline ll sub(ll x, ll y, ll m)
{
    ll z = x - y;
    if (z < 0)
        z += m;
    z %= m;
    return z;
}

ll n, m, dp[1001][1001][10];

ll rec(ll al, ll bl, ll in)
{
    if (al > n)
        return 0;
    if (bl < 0)
        return 0;
    if (al > bl)
        return 0;
    if (in >= m)
        return 1;
    if (dp[al][bl][in] != -1)
        return dp[al][bl][in];
    if (al == bl)
        return 1;
    ll k = 0;
    k = add(k, rec(al + 1, bl, in), mod);
    k = add(k, rec(al, bl - 1, in), mod);
    k = add(k, rec(al, bl, in + 1), mod);
    k = sub(k, rec(al + 1, bl - 1, in), mod);
    // cout << al << " " << bl << " " << in << " " << k << "\n";
    return dp[al][bl][in] = k;
}

