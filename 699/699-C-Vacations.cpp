const ll N = 101;
ll n, a[N], dp[N][3]; // rest, gym, contest

ll rec(ll in, ll last) {
  if (in == n + 1)
    return 0;
  if (dp[in][last] != -1)
    return dp[in][last];
  ll k = 1e18;
  // rest
  k = min(k, 1 + rec(in + 1, 0));
  // gym
  if ((a[in] == 2 || a[in] == 3) && last != 1)
    k = min(k, rec(in + 1, 1));
  // contest
  if ((a[in] == 1 || a[in] == 3) && last != 2)
    k = min(k, rec(in + 1, 2));
  return dp[in][last] = k;
}

